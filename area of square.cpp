// volume of cuboid


#include<stdio.h>

int main()
{
	float l,b,h;
	
	printf("enter value of l \n");
	scanf("%f", &l);
	
	
	printf("enter value of b \n");
	scanf("%f", &b);
	
	
	printf("enter value of h \n");
	scanf("%f", &h);

	
	printf("volume of cuboid is : %f \n", l*b*h);
	
	
	return 0;
}
